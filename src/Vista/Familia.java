    public void mostrar_jefe()
    {
        String dirr = op.select("Familia","direccion","id="+id_familia,con)[0][0].toString();
        
        String id_jefe = op.select("Familia","id_jefe","id="+id_familia,con)[0][0].toString();
        
        String datos[] = (String[])op.select("Persona","nombres, apellidos, cedula", "id="+id_jefe, con)[0];
        
        direccion.setText("Direcci√≥n: "+dirr);
        
        nombre.setText(datos[0]);
        
        apellido.setText(datos[1]);
        
        cedula.setText("C.I: "+datos[2]);
    }
    
    public void mostrar_miembros()
    {
        String id_jefe = op.select("Familia","id_jefe","id="+id_familia,con)[0][0].toString();
        
        String datos[][] = (String[][])op.select("Persona","nombres, apellidos, cedula","id_familia="+id_familia+" and id<>"+id_jefe, con);
        
        if(datos.length>0)
        {
            jLabel7.setVisible(true);
            for (int i = 0; i < datos.length; i++)
            {
                miembros[i].setNombre(datos[i][0]);
                miembros[i].setApellido(datos[i][1]);
                miembros[i].setCedula(datos[i][2]);
                miembros[i].setVisible(true);
            }           
        }
        this.repaint();
    }
    
