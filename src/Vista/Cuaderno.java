    public String getStr(String str, int tam)
    {
        String ret=str;
        int dif=tam-str.length();
        for (int i = 0; i < dif; i++) 
        {
            ret+=" ";
        }
        return ret;
    }
    
    public void llenar()
    {
        
        jTextArea1.setFont(new java.awt.Font("Arial", 1, 14));
        jTextArea1.setText("\n        "+getStr("Apellidos y Nombres: ",100)+"CÃ©dula:");
        String text=jTextArea1.getText();
        jTextArea1.setText(text+"\n______________________________________________________________________________________");
        Calendar c = Calendar.getInstance();
       
        int dia = c.get(Calendar.DATE);
        int mes = c.get(Calendar.MONTH);
        int annio = c.get(Calendar.YEAR)-16;
        
        String fecha=annio+"-"+(mes+1)+"-"+dia;
        
        
        String data[][] = (String[][])op.select_orderby("Persona","nombres, apellidos, cedula","fecha_nac<=DATE('"+fecha+"')","apellidos, nombres",con);
        
        for (int i = 0; i < data.length; i++)
        {
            String num="";
            text=jTextArea1.getText();
            if(i<9)
            {
                num=" ";
            }
            num+=((i+1)+":");
           
            jTextArea1.setText(text+"\n"+num+"     "+getStr(data[i][1]+" "+data[i][0],100)+"C.I: "+data[i][2]);
            text=jTextArea1.getText();
            jTextArea1.setText(text+"\n______________________________________________________________________________________");    

        }
        
        if(data.length==0)
        {
            jTextArea1.setText("");
            jPanel1.setVisible(false);
            JOptionPane.showMessageDialog(null, "No se encontraron registros");
        }
        
        jTextArea1.setCaretPosition(0);
    }

